// Your First C++ Program

// The iostream library is included for input and output functionality
#include <iostream>

// The main function is the entry point of any C++ program
int main()
{
    // std::cout is used to output text to the console
    // The "Hello World!" string will be printed
    std::cout << "Hello World!";

    // return 0 indicates that the program has successfully executed
    return 0;
}

// Key notes:
// 1. Every C++ program must have a main() function.
//    It is where the execution of the program begins.
// 2. The #include <iostream> line brings in the iostream library,
//    which is necessary for using std::cout (for output) and std::cin (for input).
// 3. std::cout is part of the standard namespace (std), which provides
//    access to C++ standard functionality.
// 4. The semicolon (;) marks the end of each statement in C++.
// 5. The return 0; statement in the main function signifies that the program
//    ended successfully. If you omit it in modern C++, the compiler assumes it.
// 6. "Hello World!" is a string literal and is printed exactly as it appears inside the quotes.
